function add(val1, val2) {
  return val1 + val2;
}
function subtract(val1, val2) {
  return val1 - val2;
}
function multiply(val1, val2) {
  return val1 * val2;
}
function divide(val1, val2) {
  return val1 / val2;
}
function increment(n) {
  return n + 1;
}
function decrement(n) {
  return n - 1;
}
function makeInt(n) {
  let r = 10;
  return parseInt(n, r);
}
function preserveDecimal(n) {
  n = parseFloat(n);
  return n;
}
